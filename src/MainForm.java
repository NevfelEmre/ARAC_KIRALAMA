    private void teslimEtPanelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_teslimEtPanelMousePressed
        try {
            DefaultTableModel tblModel2 = (DefaultTableModel) jTable2.getModel();
            int index = jTable2.getSelectedRow();
            String selectedNumberPlate = tblModel2.getValueAt(index, 0).toString();
            
            for (Car car : carList) {
                if (car.getNumberPlate().equals(selectedNumberPlate)) {

                    for (Customer customer : customerList) {
                        if (car.getUserID().equals(customer.getIdentity())) {
                            customer.changeAvailability();
                            customer.setNumberPlate("---");
                            customer.setDeadline("---");
                            customer.setStartDate("---");
                            customer.addToHistory(car);
                            break;
                        }
                    }
                    car.changeAvailability();
                    car.setUser("---");
                    car.setUserID("---");
                    car.setDeadline("---");
                    car.setStartDate("---");
                    break;
                }
            }
            UpdateCustomers();
            UpdateCars();
        } catch (Exception e) {
            jLabel14.setText("Lütfen araç seçiniz");
        }
    }//GEN-LAST:event_teslimEtPanelMousePressed

