    public void UpdateCars() {
        DefaultTableModel tblModel1 = (DefaultTableModel) jTable1.getModel();//müsait araçlar
        DefaultTableModel tblModel2 = (DefaultTableModel) jTable2.getModel();//kullanılan araçlar
        DefaultTableModel tblModel4 = (DefaultTableModel) jTable4.getModel();//bütün araçlar

        // <editor-fold defaultstate="collapsed" desc="Müsait araçlar tablosu güncellemesi">  
        tblModel1.setRowCount(0);

        for (Car c : carList) {
            if (c.isAvailable()) {
                String data[] = {c.getNumberPlate(), c.getTransmissionType().toString(), c.getFuelType().toString(), c.getColor(), c.getBrand(), c.getModel(), c.getYear()};
                tblModel1.addRow(data);
            }
        }
        // </editor-fold>

        // <editor-fold defaultstate="collapsed" desc="Kullanılan araçlar tablosu güncellemesi">  
        tblModel2.setRowCount(0);

        for (Car c : carList) {
            if (!c.isAvailable()) {
                String data[] = {c.getNumberPlate(), c.getTransmissionType().toString(), c.getFuelType().toString(), c.getColor(), c.getBrand(), c.getModel(), c.getYear(), c.getUser(), c.getStartDate(), c.getDeadline()};
                tblModel2.addRow(data);
            }
        }
        // </editor-fold>

        // <editor-fold defaultstate="collapsed" desc="Araçlar tablosu güncellemesi">  
        tblModel4.setRowCount(0);

        for (Car c : carList) {
            if (c.isAvailable()) {
                String data[] = {c.getNumberPlate(), c.getTransmissionType().toString(), c.getFuelType().toString(), c.getColor(), c.getBrand(), c.getModel(), c.getYear(), "Müsait", c.getStartDate(), c.getDeadline()};
                tblModel4.addRow(data);
            } else {
                String data[] = {c.getNumberPlate(), c.getTransmissionType().toString(), c.getFuelType().toString(), c.getColor(), c.getBrand(), c.getModel(), c.getYear(), "Kullanımda", c.getStartDate(), c.getDeadline()};
                tblModel4.addRow(data);
            }
        }
        // </editor-fold>
    }

