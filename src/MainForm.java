    public void LoadLists() {
        try {
            readCarsFromJSON(Car[].class);
            readCustomersFromJSON(Customer[].class);

        } catch (IOException | JsonIOException | JsonSyntaxException ex) {
        }
    }

    public void SaveLists() {
        try {
            writeCustomersToJSON();
            writeCarsToJSON();
        } catch (IOException | JsonIOException | JsonSyntaxException ex) {
        }
    }

    public void writeCustomersToJSON() throws IOException, JsonIOException {
        try ( FileWriter fileWriter = new FileWriter(FilenameCustomer)) {
            gson.toJson(customerList, fileWriter);
        }
    }

    public void readCustomersFromJSON(java.lang.reflect.Type type) throws IOException, JsonIOException, JsonSyntaxException {
        try ( FileReader fileReader = new FileReader(FilenameCustomer)) {
            customerList = new LinkedList<Customer>(Arrays.asList(gson.fromJson(fileReader, type)));
        }
    }

    public void writeCarsToJSON() throws IOException, JsonIOException {
        try ( FileWriter fileWriter = new FileWriter(FilenameCar)) {
            gson.toJson(carList, fileWriter);
        }
    }

    public void readCarsFromJSON(java.lang.reflect.Type type) throws IOException, JsonIOException, JsonSyntaxException {
        try ( FileReader fileReader = new FileReader(FilenameCar)) {
            carList = new LinkedList<Car>(Arrays.asList(gson.fromJson(fileReader, type)));
        }
    }
